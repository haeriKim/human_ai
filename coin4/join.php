                                    이용신청의 승낙

                                    회사는 회원이 회사 소정의 가입신청 양식에서 요구하는 모든 사항을 정확히 기재하여 이용신청을 한 경우 회원가입을 승낙할 수 있습니다. 단, 제2호, 제3호의 경우는 회사는 승낙을 유보하거나 승낙을 거절할 수 있습니다.

                                    회사는 다음 각 호에 해당하는 이용신청에 대하여는 승낙을 유보할 수 있습니다.

                                    가. 설비에 여유가 없는 경우

                                    나. 기타 회사의 사정상 이용승낙이 곤란한 경우

                                    회사는 다음 각 호에 해당하는 이용신청에 대하여는 이를 승낙하지 않을 수 있습니다.

                                    가. 이름이 실명이 아닌 경우

                                    나. 다른 사람의 명의를 사용하여 신청한 경우

                                    다. 이용 신청 시 필요내용을 허위로 기재하여 신청한 경우

                                    라. 사회의 안녕질서 또는 미풍양속을 저해할 목적으로 신청한 경우

                                    마. 회사가 정한 이용신청요건에 미비한 부분이 있었을 때

                                    제8조 (이용계약의 중지 및 해지)

                                    이용계약은 회원 또는 회사의 해지에 의해 종료됩니다.

                                    회원이 이용계약을 해지하고자 할 때에는 자신의 자산(암호화폐와 원화 포인트) 전부를 외부로 이전한 후 회사에 해지 신청을 하여야 합니다.

                                    회사는 회원이 아래의 사유에 해당하는 경우 상당한 기간을 정하여 최고 후 서비스 이용을 제한하거나 이용계약을 해지할 수 있습니다.

                                    - 타인의 서비스 ID 및 비밀번호를 도용한 경우

                                    - 서비스 운영을 고의로 방해한 경우

                                    - 가입한 이름이 실명이 아닌 경우

                                    - 회원이 사회적 공익을 저해할 목적으로 서비스를 이용하는 경우

                                    - 타인의 명예를 손상시키거나 불이익을 주는 행위를 한 경우

                                    - 서비스의 안정적 운영을 방해할 목적으로 다량의 정보를 전송하거나 광고성 정보를 전송하는 경우

                                    - 정보통신설비의 오작동이나 정보 등의 파괴를 유발시키는 컴퓨터 바이러스 프로그램 등을 유포하는 경우

                                    - 타인의 개인정보, 이용자ID 및 비밀번호를 부정하게 사용하는 경우

                                    - 회사의 서비스 정보를 이용하여 얻은 정보를 회사의 사전 승낙 없이 복제 또는 유통시키거나 상업적으로 이용하는 경우

                                    - 같은 사용자가 다른 ID로 이중등록을 한 경우

                                    - 회사, 다른 회원 또는 제3자의 지식재산권을 침해하는 경우

                                    - 방송통신심의위원회 등 외부기관의 시정요구가 있거나 불법선거운동과 관련하여 선거관리위원회의 유권해석을 받은 경우

                                    - 이용자에 대한 개인정보를 그 동의 없이, 수집, 저장, 공개하는 경우

                                    - 회원의 서비스 이용이 범죄와 결부된다고 객관적으로 판단되는 경우

                                    - 수수료 등 이익을 얻을 목적으로 타인을 대행하여 암호화폐를 거래 하는 경우

                                    - 회원의 행위가 기타 관계 법령에 위배되는 경우

                                    서비스 이용 중지 또는 제한 절차

                                    회사는 이용제한을 하고자 하는 경우에는 그 사유, 일시 및 기간을 정하여 서면(전자우편 포함) 또는 전화, 홈페이지의 메시지 기능 등의 방법을 이용하여 해당 회원에게 통지합니다.

                                    다만, 회사가 긴급하게 이용을 중지해야 할 필요가 있다고 인정하는 경우에는 전항의 과정 없이 서비스 이용을 제한할 수 있습니다.

                                    서비스 이용중지를 통지 받은 회원은 이용중지에 대하여 이의가 있을 경우 이의 신청을 할 수 있습니다.

                                    회사는 이용중지 기간 중에 그 이용중지 사유가 해소된 것이 확인된 경우에 한하여 이용중지 조치를 즉시 해제합니다.

