                                    제5조 (이용계약의 성립)

                                    이용자는 회사가 정한 가입 양식에 따라 회원정보를 기입한 후 이 약관에 동의한다는 의사표시를 함으로서 회원가입을 신청합니다.

                                    회원가입은 회사의 승낙이 회원에게 도달한 시점으로 합니다.

                                    이용 계약은 회원ID 단위로 체결합니다. 이용계약이 성립되면, 이용신청자는 회원으로 등록됩니다.

